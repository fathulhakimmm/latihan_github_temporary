ringan yang tersebar diseluruh wilayah Sumatera Selatan, Lampung, Jambi dan Bengkulu. Hadirnya Perseroan di tengah-tengah masyarakat dipercaya mampu memberikan manfaat baik kepada Pemerintah Pusat dan Daerah berupa pajak dan retribusi, juga kepada pemegang saham melalui pemberian dividen, dividen serta kepada masyarakat sekitar melalui penyerapan tenaga kerja lokal, maupun dalam bentuk kemitraan dan bina Lingkungan bagi masyarakat sekitar pabrik. </p>
