                            Pada saat didirikan pada 14 November 1974, Perusahaan lahir dengan nama PT Semen Baturaja (Persero) dengan kepemilikan saham sebesar 45% dimiliki oleh PT Semen Gresik dan PT Semen Padang sebesar 55%. Lima tahun kemudian, pada tanggal 9 November 1979 Perusahaan berubah status dari Penanaman Modal Dalam Negeri (PMDN) menjadi Persero dengan komposisi saham sebesar 88% dimiliki oleh Pemerintah Republik Indonesia, PT Semen Padang sebesar 7% dan PT Semen Gresik sebesar 5%. Beberapa tahun kemudian yaitu pada tahun 1991, saham Perseroan diambil alih secara penuh oleh Pemerintah Republik Indonesia. Selanjutnya Perseroan terus mengalami perkembangan sehingga pada tanggal 14 Maret 2013 PT Semen Baturaja (Persero) mengalami perubahan status menjadi Perseroan terbuka dan berubah nama menjadi PT Semen Baturaja (Persero) Tbk.</p>
